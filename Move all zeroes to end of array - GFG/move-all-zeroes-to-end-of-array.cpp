//User function template for C++
class Solution{
public:
	void pushZerosToEnd(int arr[], int n) {
	       int count_zero=0;
	       
	       for(int i=0; i<n; i++) {
	           if(arr[i] == 0) {
	               count_zero++;
	           }
	           else if(count_zero > 0) {
	               swap(arr[i], arr[i-count_zero]);
	           }
	       }
	       
	}
};

